package za.co.ntier.webform.form.bean;

public class EmployedNoInfo {
	private int employedNo = 0;
	private int unEmployedNo = 0;
	private int totalApplyNo = 0;
	/**
	 * @return the employedNo
	 */
	public int getEmployedNo() {
		return employedNo;
	}
	/**
	 * @param employedNo the employedNo to set
	 */
	public void setEmployedNo(int employedNo) {
		this.employedNo = employedNo;
	}
	/**
	 * @return the unEmployedNo
	 */
	public int getUnEmployedNo() {
		return unEmployedNo;
	}
	/**
	 * @param unEmployedNo the unEmployedNo to set
	 */
	public void setUnEmployedNo(int unEmployedNo) {
		this.unEmployedNo = unEmployedNo;
	}
	/**
	 * @return the totalApplyNo
	 */
	public int getTotalApplyNo() {
		return totalApplyNo;
	}
	/**
	 * @param totalApplyNo the totalApplyNo to set
	 */
	public void setTotalApplyNo(int totalApplyNo) {
		this.totalApplyNo = totalApplyNo;
	}
}
